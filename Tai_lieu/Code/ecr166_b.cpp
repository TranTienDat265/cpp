 #include <bits/stdc++.h>
 using namespace std;
 #define int long long
 int n;
 int a[200005],b[200005];
 main()
 {
 	ios_base::sync_with_stdio(false);cin.tie(0);cout.tie(0);
 	int t; cin >> t;
 	while (t--)
 	{
 		cin >> n;
 		for (int i=1;i<=n;i++) cin >> a[i];
 		for (int i=1;i<=n+1;i++) cin >> b[i];
 		int res=0,k=INT_MAX;
 		bool check=false;
 		for (int i=1;i<=n;i++)
 		{
 			res+=abs(a[i]-b[i]);
 			k=min({k,abs(a[i]-b[n+1]),abs(b[i]-b[n+1])});
 			if ((b[n+1]>=a[i] && b[n+1]<=b[i]) || (b[n+1]>=b[i] && b[n+1]<=a[i])) check=true;
 		}
 		if (check) cout << res+1;
 		else cout << res+k+1;
 		cout << '\n';

 	}
 }