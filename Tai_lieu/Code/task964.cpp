#include <bits/stdc++.h>
using namespace std;
#define int long long
int res = 0;
int a[5];
int a1,a2,b1,b2;
void check()
{
	int v1 = a1, v2 = a2, v3 = b1, v4 = b2;
	if (a[1]) swap(v1,v2);
	if (a[2]) swap(v3,v4);
	if (v1 > v3 && v2 > v4) res++;
	else if (v1 == v3 && v2 > v4) res++;
	else if (v1 > v3 && v2 == v4) res ++;
}

void Try(int i)
{
	if (i > 2) return check(),void();
	a[i] = 1;
	Try(i+1);
	a[i] = 0; 
	Try(i+1);
}


main()
{
	ios_base::sync_with_stdio(false);cin.tie(0);cout.tie(0);
	int t; cin >> t;
	while (t--)
	{	
		cin >> a1 >> a2 >> b1 >> b2;
		Try(1);
		cout << res << '\n';
		res = 0;

	}
}