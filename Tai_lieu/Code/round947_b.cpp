#include <bits/stdc++.h>
using namespace std;
#define int long long
int n,a[100005];
void solve()
{
	sort(a+1,a+n+1);
	unordered_map <int,bool> f;
	int ans=0,cnt=0;
	for (int i=2;i<=n;i++)
		if (a[i]%a[1]!=0) f[a[i]]=true,ans=__gcd(ans,a[i]),cnt++;
	if (cnt==0) return cout << "YES" << '\n',void();
	for (int i=1;i<=sqrt(ans);i++) if (f[i]||f[ans/i]) return cout << "YES" << '\n',void();
	cout << "NO" << '\n';
}
main()
{
	ios_base::sync_with_stdio(false);cin.tie(0);cout.tie(0);
	int t; cin >> t;
	while (t--)
	{
		cin >> n;
		for (int i=1;i<=n;i++) cin >> a[i];
		solve();
	}
}