#include <bits/stdc++.h>
using namespace std;
#define int long long
int n;
bool prime[1000006];
void sie()
{
	prime[0]=prime[1]=true;
	for (int i=2;i<=1000;i++) for (int j=i*i;j<=1e6;j+=i) prime[j]=true;
}

main()
{
	ios_base::sync_with_stdio(false);cin.tie(0);cout.tie(0);
    sie();
	int t; cin >> t;
	while (t--)
	{
		cin >> n;
		int k=trunc(sqrt(n));
		if (k*k==n && (prime[k]==false)) cout << 1;
		else cout << 0;
		cout << '\n';
	}
}