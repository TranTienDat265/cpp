#include <bits/stdc++.h>
using namespace std;
#define int long long
int m,n;

main()
{
	ios_base::sync_with_stdio(false);cin.tie(0);cout.tie(0);
	int t; cin >> t;
	while (t--)
	{
		cin >> m >> n;
		vector <int> cot(n+1,0);
		vector <int> hang(m+1,0);
		for (int i=1;i<=m;i++)
		for (int j=1;j<=n;j++)
		{
			char c; cin >> c;
			if (c=='#') cot[j]++,hang[i]++;
		}
		int x=0,y=0;
		for (int i=1;i<=m;i++) for (int j=1;j<=n;j++) if (hang[i]==cot[j])
			if (hang[i]==cot[j] && hang[i]>hang[x]) x=i,y=j;
		cout << x << ' ' << y << '\n';
	}
}