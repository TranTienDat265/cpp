#include <bits/stdc++.h>
using namespace std;
#define int long long 
int n,k;
int a[500005];
void solve()
{
	cin >> n >> k;
	for (int i = 1; i <= n; i++) cin >> a[i];
	sort(a + 1, a + n + 1);
	int mx = a[n];
	for (int i = mx; i < mx + 3 * k; i++)
	{
		int dic = 0;
		for (int j = 1, val; j <= n; j++)
		{
			val = (i - a[j] + 1) / k + ((i - a[j] + 1) % k !=0);
			if (val&1) continue;
			else dic = max(dic, (a[j] + val * k) - i);
		}
		if (dic == 0) return cout << i << '\n',void();
		else i += dic - 1;
	}
	cout << -1 << '\n';
}

main()
{
	ios_base::sync_with_stdio(false);cin.tie(0);cout.tie(0);
	int t; cin >> t;
	while (t--)
	{
		solve();

	}
}