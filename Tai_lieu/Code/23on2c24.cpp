void Try(int i, int x, int y)
{
	if (i>k) return;
	if (a[x-1][y]==0 && a[x-1][y+1]==0 && a[x-1][y-1]==0 && a[x][y-1]==0) 
	{
		a[x][y]=1;
		if (i==k) res++;
		for (int nx=x;nx<=n;nx++)
		for (int ny=(nx==x? y+1:1);ny<=n;ny++)
			Try(i+1,nx,ny);
		a[x][y]=0;
	} 
}

