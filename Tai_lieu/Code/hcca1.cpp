#include <bits/stdc++.h>
using namespace std;
#define int long long
int n;
struct dta{
	char c;
	int dif=0,id;
}a[500005];
bool cmp(dta a, dta b) 
{
	if (a.dif==b.dif) return a.id>b.id;
	return a.dif<b.dif;
}
main()
{
	ios_base::sync_with_stdio(false);cin.tie(0);cout.tie(0);
	string s;
	cin >> s;
	n=s.size();
	int op=0,cl=0;
	for (int i=1;i<=n;i++)
	{
		a[i].c=s[i-1];
		a[i].dif=op-cl;
		a[i].id=i;
		op+=(s[i-1]=='(');
		cl+=(s[i-1]==')');
	}
	sort(a+1,a+n+1,cmp);
	for (int i=1;i<=n;i++) cout << a[i].c;

}