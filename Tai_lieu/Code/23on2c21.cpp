#include <bits/stdc++.h>
using namespace std;
#define int long long 

struct dta{
	int fi,se;
}a[100005];
bool cmp(dta a, dta b)
{
	return a.fi<b.fi;
}
int val[100005],f[100005],x[100005],y[100005];
signed main()
{
	ios_base::sync_with_stdio(false);cin.tie(0);cout.tie(0);
	int n,m; cin >> n >> m;
	for (int i=1; i <= n; i++) cin >> a[i].fi >> a[i].se;
	sort(a+1, a+n+1, cmp);
	for (int i=1; i <= n; i++) f[i] = a[i].fi, val[i] = val[i-1] + a [i].se;
	for (int i=1; i <= m; i++) cin >> x[i];
	for (int i=1; i <= m; i++) cin >> y[i];
	for (int i=1; i <= m; i++)
		cout << val[upper_bound(f+1, f+n+1, x[i])-f-1] + val[upper_bound(f+1, f+n+1, y[i])-f-1] << '\n';
}