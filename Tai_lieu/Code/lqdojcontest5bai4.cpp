#define se second
const int mode=1e9+7,N=1e6+7;
void fastIO(){ios_base::sync_with_stdio(false);cin.tie(0);cout.tie(0);}
int n,k;
int dp[2222][2222];
vector <int> divs[2222];
void sie(int lim)
{
	for (int i=1;i<=lim;i++)
		for (int j=i;j<=lim;j+=i) divs[j].push_back(i);
}

void solve()
{
	for (int i=1;i<=n;i++) dp[1][i]=1;
	for (int i=2;i<=k;i++)
	for (int j=1;j<=n;j++)
	for (int l : divs[j]) 
	{
		dp[i][j]+=dp[i-1][l];
		if (dp[i][j]>=mode) dp[i][j]-=mode;
	}
	int res=0;
	for (int i=1;i<=n;i++) res+=dp[k][i];
	cout << res%mode;
}
void ReadText()
{
	cin >> n >> k;
	sie(n);
}

